public class HolaMundo {

    // Recordar que al comenzar a escribir "main"
    // el IDE ofrece la escritura automática del método.
    public static void main(String[] args) {
        String msj = "Hola Mundo!";

        // Recordar que la forma rápida de escribir System.out.println
        // es escribir "sout" y aceptar la sugerencia del IDE
        System.out.println(msj);
    }
}
